// Creator of the Week functionality
function getWeekNumber() {
  const now = new Date();
  const currentSunday = new Date(now);
  currentSunday.setHours(0, 0, 0, 0);
  currentSunday.setDate(currentSunday.getDate() - currentSunday.getDay());

  const startSunday = new Date(ROTATION_START_DATE);
  startSunday.setHours(0, 0, 0, 0);
  startSunday.setDate(startSunday.getDate() - startSunday.getDay());

  const msPerWeek = 1000 * 60 * 60 * 24 * 7;
  return Math.floor((currentSunday - startSunday) / msPerWeek);
}

function displayCreatorOfWeek() {
  const creatorShowcaseEl = document.getElementById('creator-showcase');
  if (!creatorShowcaseEl) return;

  const weekNumber = getWeekNumber();
  const offset = ((weekNumber % CYCLE_LENGTH) + CYCLE_LENGTH) % CYCLE_LENGTH;
  const creatorName = weekSchedule[offset];
  const creator = members.find(m => m.name === creatorName);
  if (!creator) return;

  const cardImage = creator.card ? `assets/Creator_Cards/${creator.card}` : "";

  const creatorNameEl = document.getElementById('creator-name');
  creatorNameEl.textContent = creator.name;
  creatorNameEl.style.backgroundImage = creator.gradient;

  const creatorHandle = getTikTokHandle(creator.tiktok);
  creatorNameEl.dataset.tiktokHandle = creatorHandle || "";

  let creatorLiveBadgeEl = document.getElementById('creator-live-badge');
  if (!creatorLiveBadgeEl) {
    creatorLiveBadgeEl = document.createElement('span');
    creatorLiveBadgeEl.id = 'creator-live-badge';
    creatorLiveBadgeEl.className = 'creator-live-badge';
    creatorNameEl.insertAdjacentElement('afterend', creatorLiveBadgeEl);
  }
  creatorLiveBadgeEl.dataset.tiktokHandle = creatorHandle || "";
  creatorLiveBadgeEl.textContent = creatorHandle ? 'Checking...' : 'No TikTok';
  creatorLiveBadgeEl.classList.toggle('live', false);

  document.getElementById('creator-skin').src = cardImage;
  document.getElementById('creator-skin').alt = `${creator.name} Creator Card`;

  if (creator.discord) {
    document.getElementById('creator-discord').href = creator.discord;
    document.getElementById('creator-discord').style.display = 'inline-flex';
  } else {
    document.getElementById('creator-discord').style.display = 'none';
  }

  document.getElementById('creator-tiktok').href = creator.tiktok;

  if (creatorHandle) {
    updateCreatorLiveStatus();
  }
}

displayCreatorOfWeek();

const pointerTarget = {
  x: window.innerWidth / 2,
  y: window.innerHeight / 2,
};

const pointerCurrent = {
  x: pointerTarget.x,
  y: pointerTarget.y,
};

const movePointerTarget = (x, y) => {
  pointerTarget.x = x;
  pointerTarget.y = y;
};

window.addEventListener("pointermove", (event) => {
  movePointerTarget(event.clientX, event.clientY);
});

window.addEventListener(
  "touchmove",
  (event) => {
    const [touch] = event.touches;
    if (touch) {
      movePointerTarget(touch.clientX, touch.clientY);
    }
  },
  { passive: true }
);

window.addEventListener("resize", () => {
  if (pointerTarget.x > window.innerWidth || pointerTarget.y > window.innerHeight) {
    movePointerTarget(window.innerWidth / 2, window.innerHeight / 2);
  }
});

window.addEventListener("pointerleave", () => {
  movePointerTarget(window.innerWidth / 2, window.innerHeight / 2);
});

const animateScene = () => {
  pointerCurrent.x += (pointerTarget.x - pointerCurrent.x) * 0.08;
  pointerCurrent.y += (pointerTarget.y - pointerCurrent.y) * 0.08;

  const pointerX = pointerCurrent.x / window.innerWidth - 0.5;
  const pointerY = pointerCurrent.y / window.innerHeight - 0.5;

  root.style.setProperty("--mouse-x", `${pointerCurrent.x}px`);
  root.style.setProperty("--mouse-y", `${pointerCurrent.y}px`);

  assets.forEach((asset) => {
    const depth = Number(asset.dataset.depth || 20);
    const spin = Number(asset.dataset.spin || 8);

    asset.style.setProperty("--shift-x", `${(-pointerX * depth).toFixed(2)}px`);
    asset.style.setProperty("--shift-y", `${(-pointerY * depth).toFixed(2)}px`);
    asset.style.setProperty(
      "--tilt",
      `${(pointerX * spin + pointerY * spin * 0.55).toFixed(2)}deg`
    );
  });

  if (mouseGlow) {
    mouseGlow.style.transform = `translate(${(pointerCurrent.x - 140).toFixed(2)}px, ${(pointerCurrent.y - 140).toFixed(2)}px)`;
  }

  requestAnimationFrame(animateScene);
};

animateScene();


function getTikTokHandle(url) {
  const match = url.match(/tiktok\.com\/@([^/?&]+)/i);
  return match ? match[1] : null;
}

function renderLiveBadge(handle) {
  if (!handle) return "";
  return `<span class="member-live-badge" data-tiktok-handle="${handle}">Checking...</span>`;
}

async function fetchAllLiveStatuses(handles) {
  if (!handles.length) return {};

  const params = new URLSearchParams();
  handles.forEach((handle) => params.append("user", handle));
  const response = await fetch(`${BACKEND_BASE_URL}/api/tiktok-live-status?${params.toString()}`);
  if (!response.ok) {
    return {};
  }

  const data = await response.json();
  if (!data.results) {
    return {};
  }

  return data.results.reduce((acc, item) => {
    if (item && item.user) {
      acc[item.user.toLowerCase()] = item.live === true;
    }
    return acc;
  }, {});
}

async function updateMemberLiveBadges() {
  const badgeElements = Array.from(document.querySelectorAll(".member-live-badge"));
  const handles = [...new Set(badgeElements.map((el) => el.dataset.tiktokHandle).filter(Boolean))];
  if (!handles.length) return;

  const statusMap = await fetchAllLiveStatuses(handles);

  badgeElements.forEach((badgeEl) => {
    const handle = badgeEl.dataset.tiktokHandle.toLowerCase();
    const isLive = statusMap[handle] === true;
    badgeEl.textContent = isLive ? "LIVE" : "Offline";
    badgeEl.classList.toggle("live", isLive);
  });
}

